package com.tazouxme.idp.security.velocity;

public class Templates {
	
	private String postSimpleSignTemplate;
	private String postTemplate;
	private String artifactTemplate;
	
	public String getPostSimpleSignTemplate() {
		return postSimpleSignTemplate;
	}
	
	public void setPostSimpleSignTemplate(String postSimpleSignTemplate) {
		this.postSimpleSignTemplate = postSimpleSignTemplate;
	}
	
	public String getPostTemplate() {
		return postTemplate;
	}
	
	public void setPostTemplate(String postTemplate) {
		this.postTemplate = postTemplate;
	}
	
	public String getArtifactTemplate() {
		return artifactTemplate;
	}
	
	public void setArtifactTemplate(String artifactTemplate) {
		this.artifactTemplate = artifactTemplate;
	}

}
